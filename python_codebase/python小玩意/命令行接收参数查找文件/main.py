files = open("./file.txt", "w+", encoding="utf-8")


def file_path(args):
    path, types_list = args[0], args[1]
    if isinstance(types_list, str):
        types_list = types_list.split(",")
    try:
        os.chdir(path)
    except PermissionError as p:
        print(p)
        return
    for dirs in os.listdir(os.getcwd()):
        file_dir_path = os.path.join(path, dirs)
        if os.path.isdir(file_dir_path):
            if os.path.exists(file_dir_path):
                file_path([file_dir_path, types_list])
        else:
            if dirs.rsplit(".", 1)[-1] in types_list:
                print(file_dir_path)
                files.write(file_dir_path + '\n')


if __name__ == '__main__':
    args = sys.argv
    if len(args) > 3:
        file_path(args[1:])
    else:
        file_path([
            input("输入目录："),
            input("输入你要的文件后缀-英文逗号分割"),
        ])

    files.flush()
    files.close()

